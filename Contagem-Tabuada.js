// Defini o número que eu quero ver a tabuada (no caso, o 9)
let Numero = 9; 

// Criei um laço que vai de 1 até 10 para multiplicar pelo meu número
for (let i = 1; i <= 10; i++) {
  // Aqui eu mostro no console a conta formatada e o resultado da multiplicação
  console.log(`${Numero} * ${i} = ${Numero*i}`);
}